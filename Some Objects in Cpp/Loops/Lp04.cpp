#include<bits/stdc++.h>
using namespace std;

// do...while loop

int main() {
// Vi du : In ra cac so tu 1 den 5

	int i = 1;
	do {
		cout<<i<<" ";
		i++;
	}while(i<=5);
/*
Notes :
- Dieu kien dung/sai thi cau lenh trong 'do' cung duoc thuc thi it nhat 1 lan
- Thuc hien cau lenh trong 'do' truoc roi moi kiem tra dieu kien trong 'while', neu sai thi ngung vong lap , dung thi vong lap duoc chay tiep
- Vi du : 
	int i = 1;
	do {
		cout<<i<<" ";
		i++;
	}while(i>=5);
=> Dieu kien sai i>=5 nhung khi chay se cho in ra ket qua la 1	
*/	
	return 0;
}

